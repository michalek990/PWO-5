package org.example.Zadanie_1;

public class Calculator {
    public int multiply(int a, int b) {
        return a * b;
    }

    public int add(int a, int b){
        return a+b;
    }

    public int subtracting(int a, int b){
        return a-b;
    }

    public int dividing(int a , int b){
        return a / b;
    }

}
